// JavaScript Document Light Box
function lightBox1(){
		var lb1 = document.getElementById("lightBox1");
		var lb1Bg = document.getElementById("lightBoxBg");
		
		lb1.style.display='block';
		lb1Bg.style.display='block';
	}
